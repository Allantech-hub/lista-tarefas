# Script para Publicação no GitHub
# Execute estes comandos no seu terminal para enviar o código

# 1. Adicionar o repositório remoto
git remote add origin https://github.com/Allantech-hub/lista-tarefas.git

# 2. Renomear a branch para main (padrão GitHub)
git branch -M main

# 3. Enviar o código (Push)
# Observação: Pode ser solicitado seu login/senha ou token do GitHub
git push -u origin main
